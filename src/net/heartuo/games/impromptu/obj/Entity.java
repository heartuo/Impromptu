package net.heartuo.games.impromptu.obj;

public class Entity {
	float width;
	float height;
	float baseWidth;
	float baseHeight;
	
	public Entity(float width, float height, float baseWidth, float baseHeight) {
		this.width = width;
		this.height = height;
		this.baseWidth = baseWidth;
		this.baseHeight = baseHeight;
	}
}
